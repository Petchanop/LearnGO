package main

import (
	"fmt"
)

func main() {
	var Ar [5]int
	for i := 0; i < 5; i++ {
		Ar[i] = i + 5
	}
	for _, val := range Ar {
		fmt.Println(val)
	}
	fmt.Printf("Type : %T\n", Ar)
}
